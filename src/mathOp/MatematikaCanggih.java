public class MatematikaCanggih extends Matematika{
    void tambah (double a, double b) {
        double hasil = a + b;
        System.out.println(a+" + "+b+" = "+hasil);
    }
    
    void tambah (int a, int b, int c) {
        hasil = a + b + c;
        System.out.println(a+" + "+b+" + "+c+" = "+hasil);
    }
    
    void tambah (double a, double b, double c) {
        double hasil = a + b + c;
        System.out.println(a+" + "+b+" + "+c+" = "+hasil);
    }
    
    void kurang (double a, double b) {
        double hasil = a - b;
        System.out.println(a+" - "+b+" = "+hasil);
    }
    
    void kurang (int a, int b, int c) {
        hasil = a - b - c;
        System.out.println(a+" - "+b+" - "+c+" = "+hasil);
    }
    
    void kurang (double a, double b, double c) {
        double hasil = a - b - c;
        System.out.println(a+" - "+b+" - "+c+" = "+hasil);
    }
    
    void kali (double a, double b) {
        double hasil = a * b;
        System.out.println(a+" x "+b+" = "+hasil);
    }
    
    void kali (int a, int b, int c) {
        hasil = a * b * c;
        System.out.println(a+" x "+b+" x "+c+" = "+hasil);
    }
    
    void kali (double a, double b, double c) {
        double hasil = a * b * c;
        System.out.println(a+" x "+b+" x "+c+" = "+hasil);
    }
    
    void bagi (double a, double b) {
        double hasil = a / b;
        System.out.println(a+" : "+b+" = "+hasil);
    }
    
    void bagi (int a, int b, int c) {
        hasil = a / b / c;
        System.out.println(a+" : "+b+" : "+c+" = "+hasil);
    }
    
    void bagi (double a, double b, double c) {
        double hasil = a / b / c;
        System.out.println(a+" : "+b+" : "+c+" = "+hasil);
    }
    
    void modulus (double a, double b) {
        double hasil = a % b;
        System.out.println(a+" % "+b+" = "+hasil);
    }
    
    void modulus (int a, int b, int c) {
        hasil = a % b % c;
        System.out.println(a+" % "+b+" % "+c+" = "+hasil);
    }
    
    void modulus (double a, double b, double c) {
        double hasil = a % b % c;
        System.out.println(a+" % "+b+" % "+c+" = "+hasil);
    }
}
